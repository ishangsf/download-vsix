    v.isPrerelease = prereleaseRegex.test(v.version);
    
    // Â¶ÇÊûúÁâàÊú¨Âè∑ÂåÖÂê´ 4 ‰ΩçÂèä‰ª•‰∏äÊï∞Â≠ó(Â¶Ç1.1.1.20220101)ÔºåÂèØËÉΩÊòØÈ¢ÑÂèëÂ∏ÉÁâàÊú¨
    const numSegments = v.version.split('.').length;
    if (numSegments > 3) {
      v.isPrerelease = true;
    }
    
    // Ê£ÄÊü•ÁâàÊú¨Âè∑ÊòØÂê¶ÂåÖÂê´‰ªª‰ΩïÈùûÊ†áÂáÜÂ≠óÁ¨¶ÔºàÂ≠óÊØçÁ≠âÔºâ
    if (!v.isPrerelease && /[a-zA-Z]/.test(v.version)) {
      v.isPrerelease = true;
    }
    
    // ÈÄöÂ∏∏ÁâàÊú¨Âè∑Ë∂ÖËøá 1.1.99 ÁöÑÂèØËÉΩÊòØÈ¢ÑÂèëÂ∏ÉÁâàÊú¨
